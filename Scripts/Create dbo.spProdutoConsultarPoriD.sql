CREATE PROCEDURE spProdutoConsultarPoriD
(
    @IDPRODUTO nvarchar(10) = NULL
)
AS
BEGIN
	SELECT IDPRODUTO,NOME,DESCRICAO,PRECO,ESTOQUE
	FROM TB_PRODUTO
	WHERE IDPRODUTO = @IDPRODUTO;
END
